package com.ark.robokart_robotics.Model;



public class AdvanceStandardModel {
    private String s_id;
    private String s_name;

    public AdvanceStandardModel(String s_id, String s_name) {
        this.s_id = s_id;
        this.s_name = s_name;
    }

    public String getS_id() {
        return s_id;
    }

    public void setS_id(String s_id) {
        this.s_id = s_id;
    }

    public String getS_name() {
        return s_name;
    }

    public void setS_name(String s_name) {
        this.s_name = s_name;
    }
}

